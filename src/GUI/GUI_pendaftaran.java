    private void BTNdaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNdaftarActionPerformed
        pendaftaran pen = new pendaftaran();
        try {
            pen.setData_Pendaftar(control.getPendaftar().get(CBpendaftar.getSelectedIndex()));
            pen.setData_Guru(control.getGuru().get(CBguru.getSelectedIndex()));
            pen.setData_Pendaftar(control.getPendaftar().get(CBpendaftar.getSelectedIndex()));
            pen.setTanggal_pendaftar(new SimpleDateFormat("dd/MM/yyyy").parse(TFtgl.getText()));
            if(control.getPendaftar().get(CBpendaftar.getSelectedIndex()).getKelas_pendaftar().equals("sma")){
                harga = 400000;
            }else if(control.getPendaftar().get(CBpendaftar.getSelectedIndex()).getKelas_pendaftar().equals("smp")){
                harga = 250000;
            }
            pen.setHarga_khursus(harga);
            control.insertPendataran(pen);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(GUI_pendaftaran.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(GUI_pendaftaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNdaftarActionPerformed

