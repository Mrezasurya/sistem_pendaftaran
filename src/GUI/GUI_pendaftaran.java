    public void showCBPendaftar() throws SQLException{
        DefaultComboBoxModel dtp = new DefaultComboBoxModel();
        for(data_pendaftar d : control.getPendaftar()){
            dtp.addElement(d.getNama_pendaftar());
        }
        CBpendaftar.removeAllItems();
        CBpendaftar.setModel(dtp);
    }
    
    public void showCBguru() throws SQLException{
        DefaultComboBoxModel dtg = new DefaultComboBoxModel();
        for(data_guru dg : control.getGuru()){
            dtg.addElement(dg.getNama_guru()+ " (" +dg.getKelas()+ ") "+dg.getJadwal());
        }
        CBguru.removeAllItems();
        CBguru.setModel(dtg);
    }
    
    public void showTBLpendaftaran() throws SQLException{
        DefaultTableModel tblpen = new DefaultTableModel(new String[]{"Nama Pendaftar", "Tgl Daftar", "Harga Khursus"},0);
        for(pendaftaran pend : control.getpendaftaran()){
            tblpen.addRow(new String[]{pend.getData_Pendaftar().getNama_pendaftar().toString(), pend.getTanggal_pendaftar().toString(), pend.getHarga_khursus().toString()});
        }
        TBLpendaftaran.setModel(tblpen);
    }

